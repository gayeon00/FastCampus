//Range
val range1 = 1..10
val range2 = 1 until 10
val range3 = 'A'..'Z'
println(range1)
println(range2)
println(range3)

//Progression
val range4 = 1..10 step 2 //1, 3, 5, 7, 9
val range5 = 10 downTo 1 step 2 //10, 8, 6, 4, 2
println(range4)
println(range5)

